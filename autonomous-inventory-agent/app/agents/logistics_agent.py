# =========================================================

def logistics_decision(quantity):

    """
    Decide estratégia logística.
    """

    # =====================================================
    # FORNECEDOR
    # =====================================================

    supplier = "Supplier A"

    # =====================================================
    # LEAD TIME
    # =====================================================

    lead_time = 5

    # =====================================================
    # ESTRATÉGIA
    # =====================================================

    strategy = "standard_shipping"

    # =====================================================
    # GRANDES PEDIDOS
    # =====================================================

    if quantity > 500:

        strategy = "priority_shipping"

        lead_time = 2

    # =====================================================
    # RETURN
    # =====================================================

    return {

        "supplier": supplier,

        "lead_time_days": lead_time,

        "strategy": strategy
    }